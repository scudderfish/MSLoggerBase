package uk.org.smithfamily.mslogger.widgets;

public class Size
{
    private int width;
    private int height;
    
    public Size(int width, int height)
    {
        super();
        this.width = width;
        this.height = height;
    }

    public int getWidth()
    {
        return width;
    }

    public int getHeight()
    {
        return height;
    }
    
    
}
